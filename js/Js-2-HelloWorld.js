// Exibindo mensagens no console e alertando na página

// Chamando no console
// O método console.log() imprime mensagens no console do navegador.
// Isso é útil para depuração e verificação do código.
// Para ver esta mensagem, abra o console do navegador pressionando F12 ou Ctrl+Shift+I.
console.log('Hello World!!!');

// Alertando na página
// O método alert() exibe uma mensagem em uma janela de alerta na página.
// Isso é usado para mostrar informações ao usuário de forma imediata.
alert('Hello World!');
